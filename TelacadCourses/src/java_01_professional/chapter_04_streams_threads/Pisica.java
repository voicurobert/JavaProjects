package java_01_professional.chapter_04_streams_threads;

public class Pisica {

    private String nume;
    private String culoare;

    public Pisica(String nume, String culoare) {
        this.nume = nume;
        this.culoare = culoare;
    }

    public String getCuloare() {
        return culoare;
    }

    public String getNume() {
        return nume;
    }
}

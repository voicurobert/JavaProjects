package java_associate.chapter_02_structuri_de_control_si_orientare_pe_obiect;

public class Exemplu3 {

    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);


    }
}
